#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long long int	c;

	c = (n > 0 ? n : -(long long int)n);
	if (n < 0)
		ft_putchar_fd('-', fd);
	if (c >= 10)
	{
		ft_putnbr_fd(c / 10, fd);
		ft_putnbr_fd(c % 10, fd);
	}
	else
		ft_putchar_fd(c + 48, fd);
}
