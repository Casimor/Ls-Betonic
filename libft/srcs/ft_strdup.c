#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		y;
	char	*str;

	i = 0;
	y = ft_strlen(src);
	str = ft_memalloc(y + 1);
	if (str == NULL)
		return (NULL);
	while (src[i])
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
